# Création d'une liste fruits
fruits = ["pomme", "banane", "orange"]
print(fruits)

# Ajouter "kiwi" à la liste fruits
fruits.append("kiwi")
print(fruits)

# Supprimer "orange" de la liste fruits
fruits.remove("orange")
print(fruits)

# Modifier le deuxième élément
fruits[1] = "ananas"
print(fruits)

# Afficher la longueur de la liste
print(len(fruits))

# Trier la liste par ordre alphabétique
fruits.sort()

# Afficher la liste après le tri
print(fruits)
